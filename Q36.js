"use strict";
// Define the function make_shirt
function make_shirt(size, message) {
    console.log(`The shirt size is ${size} and it has the message: "${message}"`);
}
// Call the function with example arguments
make_shirt('Large', 'Hello World!');
make_shirt('Medium', 'TypeScript is awesome!');
make_shirt('Small', 'Keep Calm and Code On');
